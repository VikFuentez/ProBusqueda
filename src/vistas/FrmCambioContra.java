    private void txtContraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContraKeyTyped
        char tecla = evt.getKeyChar();
        if(((tecla<'0' || tecla>'9')== tecla<'A'||tecla>'Z' && tecla<'a'||tecla>'z')&& tecla != KeyEvent.VK_BACK_SPACE){
            evt.consume();
            this.lblTexto1.setVisible(true);
            this.lblTexto1.setText("Solo se permiten A-Z, a-z y 0-9");
        }
        else
        {
            this.lblTexto1.setVisible(false);
        }
        
    }//GEN-LAST:event_txtContraKeyTyped

