    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        if(this.txtContra.getText().isEmpty() || this.txtNcontra.getText().isEmpty() || this.txtRnuevaContra.getText().isEmpty())
        {        
            JOptionPane.showMessageDialog(null, "Necesitas llenar todos los campos \npara poder guardar tu nueva contraseña"); 
        }
        
        else
        {
            boolean contrasenas=us.verificarContra(Integer.parseInt(this.lblID.getText()), this.txtContra.getText());
            if(contrasenas==true)
            {
                if(this.txtNcontra.getText().equals(this.txtRnuevaContra.getText()))
                {
                    this.btnGuardar.setEnabled(false);
                    this.panelCarga.setVisible(true);
                    timer.start();              
                }
                else
                {
                   this.lblTexto3.setVisible(true);
                   this.lblTexto3.setText("Contraseñas no coinciden");
                }
            }
            else
            {
                this.lblTexto1.setText("Contraseña erronea");
                this.lblTexto1.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

